void NaviresPage::setupTable()
{
    if (!table) {
        return;
    }

    table->setColumnCount(9);
    table->setHorizontalHeaderLabels({
        tr("ID"), tr("Nom"), tr("Matricule"), tr("Type"),
        tr("Longueur (m)"), tr("Puissance (CV)"), tr("Propriétaire"), tr("Statut"),
        tr("Actions")
    });

    table->setSelectionBehavior(QAbstractItemView::SelectRows);
    table->setEditTriggers(QAbstractItemView::NoEditTriggers);
    table->setAlternatingRowColors(true);
    table->setSortingEnabled(true);
    table->horizontalHeader()->setDefaultAlignment(Qt::AlignCenter);
    table->horizontalHeader()->setStretchLastSection(false);
    table->horizontalHeader()->setSectionResizeMode(QHeaderView::ResizeToContents);
    table->horizontalHeader()->setSectionResizeMode(1, QHeaderView::Stretch);
    table->horizontalHeader()->setSectionResizeMode(8, QHeaderView::ResizeToContents);
    table->setFrameShape(QFrame::NoFrame);
    table->verticalHeader()->setVisible(false);
    table->setShowGrid(false);
    table->setMouseTracking(true);
    table->viewport()->setMouseTracking(true);
    table->horizontalHeader()->setHighlightSections(false);

    // Hide the ID column — auto-generated by Oracle sequence, not shown to user
    table->setColumnHidden(0, true);
}

static void applyShadow(QWidget *widget, int blur, const QPoint &offset, const QColor &color, qreal opacity)
{
    if (!widget) {
        return;
    }
    auto *effect = new QGraphicsDropShadowEffect(widget);
    QColor tuned = color;
    tuned.setAlphaF(opacity);
    effect->setBlurRadius(blur);
    effect->setOffset(offset);
    effect->setColor(tuned);
    widget->setGraphicsEffect(effect);
}

