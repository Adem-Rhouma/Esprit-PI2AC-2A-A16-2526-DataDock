void NaviresPage::loadFromDb()
{
    if (!table) {
        return;
    }

    table->setSortingEnabled(false);
    table->setRowCount(0);

    Navire n;
    QSqlQueryModel *qModel = n.afficher("IDNAVIRE", "ASC");
    if (!qModel) {
        table->setSortingEnabled(true);
        return;
    }

    for (int i = 0; i < qModel->rowCount(); ++i) {
        const int id = qModel->record(i).value(0).toInt();
        const int row = table->rowCount();
        table->insertRow(row);
        table->setRowHeight(row, 50);

        // Fill data columns
        for (int col = 0; col < 7; ++col) {
            const QString cell = qModel->record(i).value(col).toString();
            auto *item = new QTableWidgetItem(cell);
            item->setTextAlignment(Qt::AlignCenter);
            table->setItem(row, col, item);
        }

        // Status column (col 7)
        const QString status = qModel->record(i).value(7).toString();
        table->setCellWidget(row, 7, createStatusBadge(status));

        // Actions column (col 8)
        table->setCellWidget(row, 8, createActionsWidget(id));
    }

    delete qModel;
    table->setSortingEnabled(true);
    applySearchFilter();
}

QWidget *NaviresPage::createActionsWidget(int idNavire)
{
    auto *wrapper = new QWidget();
    auto *layout = new QHBoxLayout(wrapper);
    layout->setContentsMargins(0, 0, 0, 0);
    layout->setSpacing(6);
    layout->setAlignment(Qt::AlignCenter);

    auto *editButton = new QToolButton(wrapper);
    editButton->setText(QStringLiteral(u"✏️"));
    editButton->setToolTip(tr("Modifier navire"));
    editButton->setObjectName("btnEdit");
    editButton->setProperty("idNavire", idNavire);

    auto *deleteButton = new QToolButton(wrapper);
    deleteButton->setText(QStringLiteral(u"🗑️"));
    deleteButton->setToolTip(tr("Supprimer navire"));
    deleteButton->setObjectName("btnDelete");
    deleteButton->setProperty("idNavire", idNavire);

    layout->addWidget(editButton);
    layout->addWidget(deleteButton);

    connect(editButton, &QToolButton::clicked, this, [this, editButton]() {
        const int id = editButton->property("idNavire").toInt();
        openEditDialog(id);
    });
    connect(deleteButton, &QToolButton::clicked, this, [this, deleteButton]() {
        const int id = deleteButton->property("idNavire").toInt();
        confirmDelete(id);
    });

    return wrapper;
}

QWidget* NaviresPage::createStatusBadge(const QString &statut)
{
    auto *wrapper = new QWidget();
    auto *layout = new QHBoxLayout(wrapper);
    layout->setContentsMargins(0,0,0,0);
    layout->setAlignment(Qt::AlignCenter);
    
    auto *label = new QLabel(statut);
    label->setAlignment(Qt::AlignCenter);
    label->setFixedSize(110, 24);
    
    QString baseStyle = "border-radius: 12px; font-weight: bold; font-size: 11px; padding: 2px 8px;";
    
    QString s = statut.toLower();
    if (s.contains("actif") || s.contains("mer") || s.contains("opération") || s.contains("active")) {
        label->setStyleSheet(baseStyle + "background-color: #e6fffa; color: #047857; border: 1px solid #047857;");
    } else if (s.contains("maintenance") || s.contains("réparation") || s.contains("panne") || s.contains("broken")) {
        label->setStyleSheet(baseStyle + "background-color: #fff5f5; color: #c53030; border: 1px solid #c53030;");
    } else if (s.contains("inactif") || s.contains("attente") || s.contains("quai")) {
        label->setStyleSheet(baseStyle + "background-color: #fffaf0; color: #b7791f; border: 1px solid #b7791f;"); // Orange/Yellow
    } else {
        label->setStyleSheet(baseStyle + "background-color: #edf2f7; color: #4a5568; border: 1px solid #4a5568;");
    }
    
    layout->addWidget(label);
    return wrapper;
}

void NaviresPage::openAddDialog()
{
    NavireCrudDialog dlg(NavireCrudDialog::AddMode, this);
    if (dlg.exec() == QDialog::Accepted) refreshTable();
}

void NaviresPage::openEditDialog(int idNavire)
{
    if (idNavire <= 0) return;
    NavireCrudDialog dlg(NavireCrudDialog::EditMode, idNavire, this);
    if (dlg.exec() == QDialog::Accepted) refreshTable();
}

void NaviresPage::confirmDelete(int idNavire)
{
    if (idNavire <= 0) return;

    QMessageBox box(QMessageBox::Question,
                    tr("Confirmation"),
                    tr("Supprimer le navire ID %1 ?").arg(idNavire),
                    QMessageBox::Yes | QMessageBox::No,
                    this);
    box.setStyleSheet(QStringLiteral(
        "QMessageBox {"
        "  background-color: #ffffff;"
        "}"
        "QLabel {"
        "  color: #111111;"
        "}"
        "QPushButton {"
        "  background-color: #325aa4;"
        "  color: #ffffff;"
        "  border: none;"
        "  border-radius: 6px;"
        "  padding: 6px 14px;"
        "  font-weight: 600;"
        "}"
        "QPushButton:hover {"
        "  background-color: #1e3a8a;"
        "}"
    ));
    const auto reply = box.exec();
    if (reply != QMessageBox::Yes) return;

    if (!Navire::supprimer(idNavire)) {
        QMessageBox errBox(QMessageBox::Warning,
                           tr("Erreur SQL"),
                           tr("Suppression impossible."),
                           QMessageBox::Ok,
                           this);
        errBox.setStyleSheet(box.styleSheet());
        errBox.exec();
        return;
    }
    refreshTable();
}

void NaviresPage::updateSort()
{
    if (!table) return;

    int column = 0;
    switch (sortCombo ? sortCombo->currentIndex() : 0) {
    case 0: column = 0; break; // ID
    case 1: column = 1; break; // Nom
    case 2: column = 2; break; // Matricule
    case 3: column = 3; break; // Type
    case 4: column = 6; break; // Proprietaire
    case 5: column = 7; break; // Statut
    default: column = 0; break;
    }

    const Qt::SortOrder order = (sortOrderCombo && sortOrderCombo->currentIndex() == 1)
                                    ? Qt::DescendingOrder
                                    : Qt::AscendingOrder;
    table->sortItems(column, order);
    table->horizontalHeader()->setSortIndicator(column, order);
}

void NaviresPage::applySearchFilter()
{
    if (!table) return;

    const QString filter = searchEdit ? searchEdit->text().trimmed() : QString();

    int column = 0;
    switch (searchCombo ? searchCombo->currentIndex() : 0) {
    case 0: column = 0; break; // ID
    case 1: column = 1; break; // Nom
    case 2: column = 2; break; // Matricule
    case 3: column = 3; break; // Type
    case 4: column = 6; break; // Proprietaire
    case 5: column = 7; break; // Statut
    default: column = 0; break;
    }

    for (int row = 0; row < table->rowCount(); ++row) {
        QTableWidgetItem *item = table->item(row, column);
        const QString text = item ? item->text() : QString();
        const bool match = filter.isEmpty() || text.contains(filter, Qt::CaseInsensitive);
        table->setRowHidden(row, !match);
    }
}

void NaviresPage::refreshTable()
{
    loadFromDb();
    updateSort();
}

void NaviresPage::exportToPDF(const QString &filePath)
{
    if (filePath.isEmpty()) return;

    QPdfWriter pdfWriter(filePath);
    pdfWriter.setPageSize(QPageSize(QPageSize::A4));
    pdfWriter.setPageOrientation(QPageLayout::Landscape);
    pdfWriter.setPageMargins(QMarginsF(15, 15, 15, 15));

    QPainter painter(&pdfWriter);
    if (!painter.isActive()) return;

    int width = pdfWriter.width();
    int height = pdfWriter.height();

    // 1. Header Background - Premium Blue Gradient Style
    painter.fillRect(0, 0, width, 1200, QColor(83, 144, 217));

    // 2. Title and Info
    painter.setPen(Qt::white);
    painter.setFont(QFont("Arial", 22, QFont::Bold));
    painter.drawText(0, 0, width, 800, Qt::AlignCenter, tr("RAPPORT DE LA FLOTTE DE NAVIRES"));
    
    painter.setFont(QFont("Arial", 10));
    painter.drawText(0, 800, width - 500, 300, Qt::AlignRight, 
                     tr("Généré le: ") + QDateTime::currentDateTime().toString("dd/MM/yyyy HH:mm"));

    // 3. Table Headers
    int y = 1400;
    int rowHeight = 450;
    QStringList headers = {tr("ID"), tr("Nom"), tr("Matricule"), tr("Type"), tr("Longueur"), tr("Puissance"), tr("Propriétaire"), tr("Statut")};
    QVector<int> colWidths = { width/15, width/6, width/8, width/8, width/10, width/10, width/6, width/12 };
    
    // Header Row Background
    painter.fillRect(0, y, width, rowHeight, QColor(240, 245, 255));
    painter.setPen(QColor(40, 60, 100));
    painter.setFont(QFont("Arial", 10, QFont::Bold));
    
    int x = 0;
    for (int i = 0; i < headers.size(); ++i) {
        painter.drawText(x + 100, y, colWidths[i] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, headers[i]);
        x += colWidths[i];
    }
    
    y += rowHeight + 100;
    painter.setPen(QColor(200, 200, 200));
    painter.drawLine(0, y - 50, width, y - 50);

    // 4. Table Data
    painter.setFont(QFont("Arial", 9));
    Navire n;
    QSqlQueryModel *model = n.afficher("IDNAVIRE", "ASC");
    if (!model) return;

    for (int i = 0; i < model->rowCount(); ++i) {
        if (y > height - 1000) {
            pdfWriter.newPage();
            y = 500;
            // Draw header again on new page if desired, or just continue
        }

        // Alternating row background for better readability
        if (i % 2 == 1) {
            painter.fillRect(0, y, width, rowHeight, QColor(252, 253, 255));
        }

        painter.setPen(QColor(50, 50, 50));
        x = 0;
        
        // Data columns (0-6)
        painter.drawText(x + 100, y, colWidths[0] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(0).toString()); x += colWidths[0];
        painter.drawText(x + 100, y, colWidths[1] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(1).toString()); x += colWidths[1];
        painter.drawText(x + 100, y, colWidths[2] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(2).toString()); x += colWidths[2];
        painter.drawText(x + 100, y, colWidths[3] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(3).toString()); x += colWidths[3];
        painter.drawText(x + 100, y, colWidths[4] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(4).toString() + " m"); x += colWidths[4];
        painter.drawText(x + 100, y, colWidths[5] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(5).toString() + " CV"); x += colWidths[5];
        painter.drawText(x + 100, y, colWidths[6] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, model->record(i).value(6).toString()); x += colWidths[6];
        
        // Status with color coding (column 7)
        QString status = model->record(i).value(7).toString();
        QString s = status.toLower();
        QColor statusColor(80, 80, 80); // Default
        
        if (s.contains("actif") || s.contains("mer") || s.contains("opération") || s.contains("active")) {
            statusColor = QColor(0, 150, 0); // Solid Green for PDF
        } else if (s.contains("maintenance") || s.contains("réparation") || s.contains("panne") || s.contains("broken")) {
            statusColor = QColor(200, 0, 0); // Solid Red for PDF
        } else if (s.contains("inactif") || s.contains("attente") || s.contains("quai")) {
            statusColor = QColor(200, 120, 0); // Solid Orange for PDF
        }
        
        painter.setPen(statusColor);
        painter.setFont(QFont("Arial", 9, QFont::Bold));
        painter.drawText(x + 100, y, colWidths[7] - 200, rowHeight, Qt::AlignVCenter | Qt::AlignLeft, status);
        painter.setFont(QFont("Arial", 9)); // Reset font
        
        y += rowHeight;
    }
    delete model;

    painter.end();
    QMessageBox::information(this, tr("Export PDF"), tr("Le rapport PDF premium coloré a été généré avec succès."));
}

void NaviresPage::exportToCSV(const QString &filePath)
{
    QFile file(filePath);
    if (!file.open(QIODevice::WriteOnly | QIODevice::Text)) return;

    QTextStream out(&file);
    out.setGenerateByteOrderMark(true);

    // Header
    out << "ID;Nom;Matricule;Type;Longueur (m);Puissance (CV);Proprietaire;Statut\n";

    Navire n;
    QSqlQueryModel *model = n.afficher("IDNAVIRE", "ASC");
    if (model) {
        for (int i = 0; i < model->rowCount(); ++i) {
            out << model->record(i).value(0).toString() << ";"
                << model->record(i).value(1).toString() << ";"
                << model->record(i).value(2).toString() << ";"
                << model->record(i).value(3).toString() << ";"
                << model->record(i).value(4).toString() << ";"
                << model->record(i).value(5).toString() << ";"
                << model->record(i).value(6).toString() << ";"
                << model->record(i).value(7).toString() << "\n";
        }
        delete model;
    }

    file.close();
    QMessageBox::information(this, tr("Export CSV"), tr("Le fichier CSV a été généré avec succès."));
}
